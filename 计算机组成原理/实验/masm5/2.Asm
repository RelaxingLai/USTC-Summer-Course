DATA SEGMENT
	BUFFER DB 10 DUP(?)
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV BX,OFFSET BUFFER
	MOV AH,1
	INT 21H
	CMP AL,32
	JNZ EXIT   ;不相等则转移
NEXT:
	MOV AH,1
	INT 21H
	CMP AL,32
	JZ OUT_ALL ;相等则转移
	MOV [BX],AL;否则保存
	INC BX
	JMP NEXT
OUT_ALL:
	MOV DL,13
	MOV AH,2
	INT 21H
	MOV DL,10
	MOV AH,2
	INT 21H
	MOV BYTE PTR[BX],'$'
	MOV DX,OFFSET BUFFER
	MOV AH,9
	INT 21H
EXIT:
	MOV AH,4CH
	INT 21H
CODE ENDS
	END START
	
	